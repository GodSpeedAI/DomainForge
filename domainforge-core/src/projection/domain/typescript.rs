/// Convert a snake_case identifier to camelCase (TS method convention).
fn camel(snake: &str) -> String {
    let mut out = String::new();
    let mut up = false;
    for ch in snake.chars() {
        if ch == '_' {
            up = true;
        } else if up {
            out.push(ch.to_ascii_uppercase());
            up = false;
        } else {
            out.push(ch);
        }
    }
    out
}

fn to_lower_first(s: &str) -> String {
    let mut c = s.chars();
    match c.next() {
        Some(first) => first.to_ascii_lowercase().to_string() + c.as_str(),
        None => String::new(),
    }
}

/// Temporary helper so `aggregates()` can reference the method name cleanly;
/// returns the snake_case method name (camel is applied at the call site).
#[cfg(test)]
mod tests {
    use super::*;
    use crate::parser::parse_to_graph;

    const FIXED_TS: &str = "2026-07-02T00:00:00+00:00";

    fn fixture_files() -> BTreeMap<String, String> {
        let src = std::fs::read_to_string(
            std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
                .join("../fixtures/projection_cell/basic/model.sea"),
        )
        .expect("fixture");
        let graph = parse_to_graph(&src).expect("parses");
        project_domain_typescript_in_memory(&graph, "test.sea", Some(FIXED_TS.to_string()))
            .expect("projects")
    }

    #[test]
    fn emits_expected_file_set() {
        let files = fixture_files();
        let keys: Vec<&str> = files.keys().map(|s| s.as_str()).collect();
        assert!(keys.contains(&"package.json"), "missing package.json");
        assert!(keys.contains(&"tsconfig.json"), "missing tsconfig.json");
        assert!(
            keys.contains(&"src/domain/aggregates.ts"),
            "missing aggregates"
        );
        assert!(
            keys.contains(&"src/ports/repositories.ts"),
            "missing repositories"
        );
        assert!(keys.contains(&"src/smoke.ts"), "missing smoke");
    }

    #[test]
    fn key_content_present() {
        let files = fixture_files();
        let repos = &files["src/ports/repositories.ts"];
        assert!(
            repos.contains("interface PurchaseOrderRepository"),
            "missing PurchaseOrderRepository"
        );
        let errs = &files["src/domain/errors.ts"];
        assert!(
            errs.contains("class RequireApprovalViolation extends DomainError"),
            "missing error"
        );
    }

    #[test]
    fn deterministic() {
        assert_eq!(fixture_files(), fixture_files());
    }

    #[test]
    fn zero_runtime_deps() {
        let files = fixture_files();
        let pj = &files["package.json"];
        assert!(pj.contains("\"private\": true"), "must be private");
        assert!(
            !pj.contains("\"dependencies\""),
            "must declare no runtime dependencies"
        );
    }
}
